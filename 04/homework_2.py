# Напишите рекурсивную функцию для вычисления суммы всех элементов в списке.
# Убедитесь, что у функции есть базовый случай.


def sum_list(lst):
    # Базовый случай: если список пуст, возвращаем 0
    if not lst:
        return 0
    else:
        # Рекурсивный случай: первый элемент + сумма остальных элементов
        return lst[0] + sum_list(lst[1:])


# Пример:
print(sum_list([1, 2, 3, 4]))
print(sum_list([]))
print(sum_list([5]))
