# Реализуйте функцию, которая находит количество способов разбить число n на сумму чисел, используя динамическое
# программирование.


def count_partitions(n):
    """
    Находит количество способов разбить число n на сумму натуральных чисел.
    Временная сложность: O(n**2), пространственная сложность: O(n**2).
    """
    # Создаём таблицу dp размером (n+1) x (n+1)
    dp = [[0] * (n + 1) for _ in range(n + 1)]

    # Базовый случай: одно разбиение для нуля
    for j in range(n + 1):
        dp[0][j] = 1

    # Заполняем таблицу dp
    for i in range(1, n + 1):  # для каждого числа от 1 до n
        for j in range(1, n + 1):  # для каждого максимального слагаемого от 1 до n
            if j > i:
                # Если j > i, то используем только числа до i
                dp[i][j] = dp[i][i]
            else:
                # Рекуррентное соотношение
                dp[i][j] = dp[i][j - 1] + dp[i - j][j]

    return dp[n][n]


def count_partitions_optimized(n):
    """
    Оптимизированная версия с использованием одномерного массива.
    Временная сложность: O(n**2), пространственная сложность: O(n).
    """
    dp = [0] * (n + 1)
    dp[0] = 1  # одно разбиение для нуля

    # Для каждого возможного слагаемого
    for num in range(1, n + 1):
        # Обновляем dp для всех чисел от num до n
        for i in range(num, n + 1):
            dp[i] += dp[i - num]

    return dp[n]


# Пример использования
n = 3000
result = count_partitions(n)
print("Базовая функция:")
print(f"Количество разбиений числа {n}: {result}")

result_opt = count_partitions_optimized(n)
print("\nОптимизированная функция:")
print(f"Количество разбиений числа {n}: {result_opt}")
