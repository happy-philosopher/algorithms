# Смоделируйте простой процесс обработки задач в очереди. Допустим, у вас есть список задач, каждая из которых занимает
# определенное время на выполнение. Используйте очередь для обработки задач и выводите время, когда каждая задача будет
# завершена.


class Queue:
    """Класс, реализующий очередь на основе списка."""

    def __init__(self):
        """Инициализирует пустую очередь."""
        self.items = []

    def enqueue(self, item):
        """Добавляет элемент в конец очереди."""
        self.items.append(item)

    def dequeue(self):
        """
        Удаляет и возвращает первый элемент очереди. Если очередь пуста, вызывает исключение IndexError.
        """
        if self.is_empty():
            raise IndexError("Нельзя удалить элемент из пустой очереди!")
        return self.items.pop(0)

    def is_empty(self):
        """Возвращает True, если очередь пуста, иначе False."""
        return len(self.items) == 0

    def peek(self):
        """
        Возвращает первый элемент очереди без удаления. Если очередь пуста, вызывает исключение IndexError.
        """
        if self.is_empty():
            raise IndexError("Нельзя посмотреть элемент пустой очереди!")
        return self.items[0]



def process_tasks_with_queue(tasks):
    """
    Обрабатывает задачи в порядке очереди с использованием класса Queue и выводит время завершения каждой.
    Args:
        tasks (list of tuples): Список кортежей (название_задачи, время_выполнения).
    """
    # Создаём очередь и добавляем в неё все задачи
    task_queue = Queue()
    for task in tasks:
        task_queue.enqueue(task)

    current_time = 0  # Начальное время (старт)

    print("Начало обработки задач:\n")

    # Пока в очереди есть задачи
    while not task_queue.is_empty():
        # Берём первую задачу из очереди
        task_name, task_duration = task_queue.dequeue()

        # Вычисляем время завершения текущей задачи
        completion_time = current_time + task_duration

        # Выводим информацию о завершении задачи
        print(f"Задача '{task_name}' завершена в {completion_time} ед. времени "
              f"(выполнялась {task_duration} ед. времени)")

        # Обновляем текущее время — теперь оно равно времени завершения последней задачи
        current_time = completion_time

    print(f"\nВсе задачи обработаны. Общее время: {current_time} ед. времени.")


# Пример использования
if __name__ == "__main__":
    # Список задач: (название, время выполнения в условных единицах)
    tasks_list = [
        ("Написать черновик статьи", 5),
        ("Проверить орфографию", 1),
        ("Добавить иллюстрации", 3),
        ("Опубликовать статью", 1),
        ("Ответить на комментарии", 4)
    ]

    process_tasks_with_queue(tasks_list)
